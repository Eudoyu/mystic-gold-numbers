export const getArticleBySlug = (slug: string): BlogArticle | undefined => {
  return blogArticles.find(article => article.slug === slug);
};

export const getFeaturedArticles = (): BlogArticle[] => {
  return blogArticles.filter(article => article.featured);
};

export const getArticlesByCategory = (category: string): BlogArticle[] => {
  return blogArticles.filter(article => article.category === category);
};

export const getAllCategories = (): string[] => {
  return [...new Set(blogArticles.map(article => article.category))];
};

export const getRelatedArticles = (currentSlug: string, limit: number = 3): BlogArticle[] => {
  const current = getArticleBySlug(currentSlug);
  if (!current) return [];
  
  return blogArticles
    .filter(article => article.slug !== currentSlug)
    .filter(article => 
      article.category === current.category || 
      article.tags.some(tag => current.tags.includes(tag))
    )
    .slice(0, limit);
};
